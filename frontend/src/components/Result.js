import React from "react";

// ── Severity config ──────────────────────────────────────────────
const SEVERITY_STYLES = {
  None:     { bg: "#d1fae5", color: "#065f46", emoji: "✅" },
  Low:      { bg: "#d1fae5", color: "#065f46", emoji: "🟢" },
  Moderate: { bg: "#fef3c7", color: "#92400e", emoji: "🟡" },
  High:     { bg: "#fee2e2", color: "#991b1b", emoji: "🔴" },
  Critical: { bg: "#fce7f3", color: "#831843", emoji: "🚨" },
};

// ── Confidence bar ───────────────────────────────────────────────
function ConfidenceBar({ value }) {
  const pct = Math.round(value * 100);
  const color = pct >= 70 ? "#22c55e" : pct >= 45 ? "#f59e0b" : "#ef4444";
  return (
    <div style={{ display: "flex", alignItems: "center", gap: 8 }}>
      <div style={{
        flex: 1, height: 8, borderRadius: 4,
        background: "rgba(255,255,255,0.15)", overflow: "hidden"
      }}>
        <div style={{ width: `${pct}%`, height: "100%", background: color, borderRadius: 4 }} />
      </div>
      <span style={{ fontSize: 12, minWidth: 36, color: "#e2e8f0" }}>{pct}%</span>
    </div>
  );
}

// ── PDF/Text export ───────────────────────────────────────────────
function downloadReport(data, summary) {
  const meta = summary.image_metadata || {};
  const severity = summary.severity || {};
  const lines = [
    "UNDERWATER TRASH DETECTION REPORT",
    "==================================",
    "",
    "IMAGE METADATA",
    "--------------",
    `File:         ${meta.filename || "N/A"}`,
    `Resolution:   ${meta.resolution || "N/A"}`,
    `Format:       ${meta.format || "N/A"}`,
    `Processed At: ${meta.processed_at || "N/A"}`,
    `Processing:   ${summary.processing_time_ms} ms`,
    "",
    "DETECTION SUMMARY",
    "-----------------",
    `Total Objects:      ${summary.total_objects}`,
    `Most Frequent:      ${summary.most_frequent}`,
    `Avg Confidence:     ${(summary.average_confidence * 100).toFixed(1)}%`,
    `Max Confidence:     ${(summary.max_confidence * 100).toFixed(1)}%`,
    `Min Confidence:     ${(summary.min_confidence * 100).toFixed(1)}%`,
    `Pollution Severity: ${severity.level || "N/A"}`,
    "",
    "OBJECT BREAKDOWN",
    "----------------",
    ...Object.entries(summary.object_counts || {}).map(
      ([k, v]) => `  ${k.padEnd(16)} Count: ${v}   Avg Conf: ${summary.class_avg_confidence?.[k] ?? "N/A"}%`
    ),
    "",
    "DETECTIONS (with bounding boxes)",
    "---------------------------------",
    ...data.detections.map((d, i) =>
      `  #${i + 1} ${d.label.padEnd(12)} Conf: ${d.confidence_pct}   ` +
      `BBox: [${d.bbox.x1}, ${d.bbox.y1}] to [${d.bbox.x2}, ${d.bbox.y2}]`
    ),
    "",
    "==================================",
    "Generated by Underwater Vision App",
  ];

  const blob = new Blob([lines.join("\n")], { type: "text/plain" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = `detection_report_${Date.now()}.txt`;
  a.click();
  URL.revokeObjectURL(url);
}

// ── Main Component ───────────────────────────────────────────────
function Result({ data }) {
  const summary = data.summary;
  const meta = summary?.image_metadata || {};
  const severity = summary?.severity || {};
  const sevStyle = SEVERITY_STYLES[severity.level] || SEVERITY_STYLES["None"];

  return (
    <div style={{ fontFamily: "sans-serif" }}>
      <h2 style={{ color: "#e2e8f0", marginBottom: 20 }}>Results</h2>

      {/* Image Grid */}
      <div className="result-grid">
        {[
          { label: "Original", src: data.original_image },
          { label: "Enhanced", src: data.enhanced_image },
          { label: "Detected", src: data.detected_image },
        ].map(({ label, src }) => (
          <div className="result-card" key={label}>
            <h3>{label}</h3>
            <img src={src} alt={label} style={{ width: "100%", borderRadius: 8 }} />
          </div>
        ))}
      </div>

      {summary && (
        <div className="summary-container">

          {/* Header: severity badge + download button */}
          <div style={{ display: "flex", justifyContent: "space-between", alignItems: "center", flexWrap: "wrap", gap: 12, marginBottom: 20 }}>
            <h2 style={{ margin: 0, color: "#e2e8f0" }}>Detection Summary</h2>
            <div style={{ display: "flex", gap: 10, alignItems: "center" }}>
              <span style={{
                padding: "4px 14px", borderRadius: 20, fontWeight: 700, fontSize: 14,
                background: sevStyle.bg, color: sevStyle.color
              }}>
                {sevStyle.emoji} {severity.level} Pollution
              </span>
              <button
                onClick={() => downloadReport(data, summary)}
                style={{
                  padding: "6px 16px", borderRadius: 8, border: "none",
                  background: "#3b82f6", color: "#fff", cursor: "pointer", fontWeight: 600
                }}
              >
                Download Report
              </button>
            </div>
          </div>

          {/* Image Metadata */}
          <div style={{
            background: "rgba(255,255,255,0.07)", borderRadius: 10,
            padding: "12px 18px", marginBottom: 18, fontSize: 13, color: "#cbd5e1"
          }}>
            <strong style={{ color: "#e2e8f0" }}>Image Info</strong>
            <div style={{ display: "flex", flexWrap: "wrap", gap: "6px 24px", marginTop: 6 }}>
              <span>File: <b>{meta.filename || "N/A"}</b></span>
              <span>Resolution: <b>{meta.resolution || "N/A"}</b></span>
              <span>Format: <b>{meta.format || "N/A"}</b></span>
              <span>Processed: <b>{meta.processed_at || "N/A"}</b></span>
              <span>Processing time: <b>{summary.processing_time_ms} ms</b></span>
            </div>
          </div>

          {/* Stat Cards */}
          <div className="summary-grid">
            {[
              { label: "Total Objects",  value: summary.total_objects },
              { label: "Most Frequent",  value: summary.most_frequent },
              { label: "Avg Confidence", value: `${(summary.average_confidence * 100).toFixed(1)}%` },
              { label: "Max Confidence", value: `${(summary.max_confidence * 100).toFixed(1)}%` },
              { label: "Min Confidence", value: `${(summary.min_confidence * 100).toFixed(1)}%` },
            ].map(({ label, value }) => (
              <div className="summary-card" key={label}>
                <h4>{label}</h4>
                <p>{value}</p>
              </div>
            ))}
          </div>

          {/* Object Breakdown with per-class confidence bars */}
          <div className="breakdown" style={{ marginTop: 20 }}>
            <h3 style={{ color: "#e2e8f0" }}>Object Breakdown</h3>
            {Object.entries(summary.object_counts).map(([key, value]) => (
              <div key={key} style={{ marginBottom: 14 }}>
                <div className="breakdown-item" style={{ marginBottom: 4 }}>
                  <span style={{ textTransform: "capitalize", fontWeight: 600 }}>{key}</span>
                  <span>{value} detected</span>
                </div>
                <div style={{ fontSize: 12, color: "#94a3b8", marginBottom: 4 }}>
                  Avg confidence for class:
                </div>
                <ConfidenceBar value={(summary.class_avg_confidence?.[key] ?? 0) / 100} />
              </div>
            ))}
          </div>

          {/* Per-Detection Table */}
          {data.detections?.length > 0 && (
            <div style={{ marginTop: 24 }}>
              <h3 style={{ color: "#e2e8f0" }}>All Detections</h3>
              <div style={{ overflowX: "auto" }}>
                <table style={{ width: "100%", borderCollapse: "collapse", fontSize: 13, color: "#cbd5e1" }}>
                  <thead>
                    <tr style={{ borderBottom: "1px solid rgba(255,255,255,0.15)", color: "#94a3b8" }}>
                      <th style={{ padding: "8px 12px", textAlign: "left" }}>#</th>
                      <th style={{ padding: "8px 12px", textAlign: "left" }}>Label</th>
                      <th style={{ padding: "8px 12px", textAlign: "left" }}>Confidence</th>
                      <th style={{ padding: "8px 12px", textAlign: "left" }}>Bounding Box (x1,y1 → x2,y2)</th>
                      <th style={{ padding: "8px 12px", textAlign: "left" }}>Size (w × h)</th>
                    </tr>
                  </thead>
                  <tbody>
                    {data.detections.map((d, i) => (
                      <tr key={i} style={{ borderBottom: "1px solid rgba(255,255,255,0.07)" }}>
                        <td style={{ padding: "8px 12px" }}>{i + 1}</td>
                        <td style={{ padding: "8px 12px", textTransform: "capitalize", fontWeight: 600 }}>{d.label}</td>
                        <td style={{ padding: "8px 12px" }}>
                          <ConfidenceBar value={d.confidence} />
                        </td>
                        <td style={{ padding: "8px 12px", fontFamily: "monospace", fontSize: 12 }}>
                          ({d.bbox.x1}, {d.bbox.y1}) → ({d.bbox.x2}, {d.bbox.y2})
                        </td>
                        <td style={{ padding: "8px 12px", fontFamily: "monospace", fontSize: 12 }}>
                          {d.bbox.width} × {d.bbox.height}
                        </td>
                      </tr>
                    ))}
                  </tbody>
                </table>
              </div>
            </div>
          )}

          {/* Environmental Insight */}
          <div className="insight" style={{ marginTop: 24 }}>
            <h3>Environmental Insight</h3>
            <p>
              {severity.level === "Critical"
                ? "Critical pollution levels detected. Immediate environmental intervention is recommended."
                : severity.level === "High"
                ? "High concentration of underwater debris. This area warrants priority cleanup attention."
                : severity.level === "Moderate"
                ? "Moderate level of objects detected. Continued monitoring is advised."
                : severity.level === "Low"
                ? "Low pollution detected. Conditions appear relatively clean."
                : "No significant underwater objects detected in this image."}
            </p>
          </div>

        </div>
      )}
    </div>
  );
}

export default Result;